import Head from 'next/head';
import Image from 'next/image';
import { Fragment } from 'react';

import Banner from '../components/Banner';
import Card from '../components/card';

import styles from '../styles/Home.module.css';

export async function getStaticProps(context) {
	const options = {
		method: 'GET',
		headers: {
		  accept: 'application/json',
		  Authorization: process.env.FOURSQUARE_API_KEY
		}
	};

	let data;
	
	try {
		const response = await fetch('https://api.foursquare.com/v3/places/search?query=coffee&ll=-33.7896601%2C18.9521995&limit=6', options);

		data = await response.json();
		console.log(data)
	} catch (err) {
		console.error(err);
	}
	
	return {
		props: {
			coffeeStores: data.results,
		}
	}
}

export default function Home({coffeeStores}) {
	const handleOnBannerBtnClick = () => {
		console.log('Hi from banner button')
	}
	return (
		<div className={styles.container}>
		<Head>
			<title>Coffee Connoisseur</title>
			<meta name="description" content="Generated by create next app" />
			<meta name="viewport" content="width=device-width, initial-scale=1" />
			<link rel="icon" href="/favicon.ico" />
		</Head>
		<main className={styles.main}>
			<Banner
				buttonText='View stores nearby'
				handleOnClick={handleOnBannerBtnClick}
			/>
			<div className={styles.heroImage}>
				<Image src='/static/hero-image.png' width={700} height={400} alt='banner image'/>
			</div>
			{coffeeStores.length > 0 &&
			<Fragment>
				<h2 className={styles.heading2}>Toronto stores</h2>
				<div className={styles.cardLayout}>
					{coffeeStores.map((coffeeStore) => (
						<Card
							key={coffeeStore.fsq_id}
							name={coffeeStore.name}
							imgUrl={coffeeStore.imgUrl}
							href={`coffee-store/${coffeeStore.fsq_id}`}
							className={styles.card}
						/>
					))}

				</div>
			</Fragment>}
		</main>
		</div>
	)
}
